def seed():
    print("=== 🧠 Agent记忆市场 - 种子数据导入 ===\n")

    # 1. 注册卖家
    print("1. 注册卖家...")
    resp = httpx.post(f"{API}/agents", json=SELLER)
    data = resp.json()
    api_key = data["api_key"]
    print(f"   ✅ 卖家: {data['name']} (余额: {data['credits']}积分)")

    # 2. 批量导入记忆
    print(f"\n2. 导入 {len(ALL_MEMORIES)} 条记忆...")
    success = 0
    fail = 0
    categories = {}

    for i, mem in enumerate(ALL_MEMORIES):
        try:
            resp = httpx.post(
                f"{API}/memories",
                json=mem,
                headers={"X-API-Key": api_key},
                timeout=10
            )
            if resp.status_code == 200:
                success += 1
                cat = mem["category"].split("/")[0]
                categories[cat] = categories.get(cat, 0) + 1
                if (i + 1) % 50 == 0:
                    print(f"   进度: {i+1}/{len(ALL_MEMORIES)}")
            else:
                fail += 1
                print(f"   ❌ {mem['title']}: {resp.text[:50]}")
        except Exception as e:
            fail += 1
            print(f"   ❌ {mem['title']}: {e}")

    print(f"\n✅ 导入完成: 成功 {success} 条, 失败 {fail} 条")

    # 3. 统计
    print("\n3. 分类统计:")
    for cat, count in sorted(categories.items(), key=lambda x: -x[1]):
        print(f"   📊 {cat}: {count}条")

    # 4. 验证
    print("\n4. 验证导入结果...")
    resp = httpx.get(f"{API}/memories", headers={"X-API-Key": api_key})
    total = resp.json()["total"]
    print(f"   📦 市场总记忆数: {total} 条")

    # 5. 保存API Key
    with open("scripts/seed_seller_key.txt", "w") as f:
        f.write(api_key)
    print(f"\n5. API Key已保存: scripts/seed_seller_key.txt")

    print("\n=== ✅ 种子数据导入完成 ===")

if __name__ == "__main__":
    seed()
